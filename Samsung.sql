DROP TABLE CUSTOMER;
alter session set NLS_DATE_FORMAT='DD/MM/YYYY'; 

CREATE TABLE CUSTOMER 
(
  Customer_id              NUMBER(4) PRIMARY KEY,
  Customer_name            VARCHAR2(40),
  Customer_dob             Date,
  Customer_gender          CHAR(1),
  Customer_phone_num       NUMBER(20)
  
);

CREATE TABLE BRANCH (
   Branch_id         NUMBER(20) PRIMARY KEY,
   Branch_name       Char(50) UNIQUE,
   Branch_phone_num  NUMBER(20) UNIQUE,
   HOB               NUMBER(5) UNIQUE
);

CREATE TABLE PRODUCT (
   Product_id         NUMBER(4) PRIMARY KEY,
   Product_name       Char(30) NOT NULL,
   Product_price      NUMBER(6) NOT NULL,
   Branch_id          NUMBER(20),
FOREIGN KEY (Branch_id) references BRANCH(Branch_id) On delete cascade
);

CREATE TABLE DEPARTMENT (
   Department_id   	    NUMBER(4) PRIMARY KEY,
   Department_name 	    VARCHAR(30),
   Num_of_workers  	    NUMBER(1) NOT NULL,
   Department_phone_num NUMBER(20),
   Branch_id        	NUMBER(20),
   HOD   	            NUMBER(5),
FOREIGN KEY (Branch_id) references BRANCH(Branch_id) On delete cascade
);

CREATE TABLE STAFF (
  Staff_id        NUMBER(5) PRIMARY KEY,
  Staff_name      VARCHAR2(60),
  Staff_dob       DATE,
  Staff_address   VARCHAR2(100),
  Staff_phone_num NUMBER(12) Unique,
  Staff_gender    CHAR(1) Not Null,
  Staff_position  CHAR(20) Not Null,
  Date_hired      DATE,
  Department_id   NUMBER(4), Check (Department_id between 5000 and 5004),
FOREIGN KEY (Department_id) references DEPARTMENT(Department_id) On delete cascade
);

CREATE TABLE TRANSACTION (
   Transaction_id      Number(4) PRIMARY KEY,
   Product_id          NUMBER(4) Check (Product_id between 2000 and 2004),
   Product_price       NUMBER(6) Not Null,
   Customer_id         Number(4) Check (Customer_id between 1000 and 1070), 
   Transaction_date    DATE,
FOREIGN KEY (Product_id) references PRODUCT(Product_id),
FOREIGN KEY (Customer_id) references CUSTOMER(Customer_id) on delete cascade
);

CREATE TABLE RETURN (
   Return_id           NUMBER(4) PRIMARY KEY,
   Transaction_id      NUMBER(4) UNIQUE,
   Product_price       NUMBER(6) NOT NULL,
   Customer_id         NUMBER(4) Check (Customer_id between 1000 and 1070),
   Transaction_date    DATE,
   Return_date         DATE,
FOREIGN KEY (Transaction_id) references TRANSACTION(Transaction_id),
FOREIGN KEY (Customer_id) references CUSTOMER(Customer_id) on delete cascade
);
CREATE TABLE INSURANCE_COMPENSATION (
   Compensation_id           NUMBER(4) PRIMARY KEY,
   Transaction_id            NUMBER(4),
   Compensation_price        NUMBER(6),
   Customer_id               NUMBER(4) Check (Customer_id between 1000 and 1070),
   Compensation_date         DATE,
FOREIGN KEY (Transaction_id) references TRANSACTION(Transaction_id),
FOREIGN KEY (Customer_id) references CUSTOMER(Customer_id) on delete cascade
);

